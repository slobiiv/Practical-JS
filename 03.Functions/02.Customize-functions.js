// Customizing functions

  function makeSandwichWith(filling) { // we have a filling as a parameter
    console.log('Get one slice of bread');
    console.log('Add ' + filling);
    console.log('Put a slice of bread on top.');
  }
  makeSandwichWith('turkey'); // and 'turkey' as argument of filling
  makeSandwichWith('ham');
  makeSandwichWith('avocado');


